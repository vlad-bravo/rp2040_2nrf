import board
import busio
import digitalio
import time
from adafruit_ov7670 import OV7670, OV7670_SIZE_DIV1, OV7670_COLOR_YUV

# === 1. Настройка тактирования (MCLK) на 8 MHz ===
# Библиотека ожидает частоту в герцах.
# Pico может генерировать не все частоты идеально точно, но 8 MHz возможна.
MCLK_FREQUENCY = 8_000_000  # 8 MHz

# === 2. Инициализация шины I2C для конфигурации камеры ===
# Важно: перед созданием шины, убедитесь, что камера выключена (shutdown)
shutdown = digitalio.DigitalInOut(board.D39) # Пример пина, подставьте ваш
shutdown.switch_to_output(True)
time.sleep(0.1)

# Создаем шину I2C на ваших пинах (пример: GP1 - SDA, GP0 - SCL)
i2c_bus = busio.I2C(board.GP1, board.GP0)

# === 3. Инициализация камеры ===
# Сопоставьте пины в соответствии с вашим проектом.
# data_pins: список из 8 пинов в порядке от D0 до D7.
cam = OV7670(
    i2c_bus,
    data_pins=[board.GP2, board.GP3, board.GP4, board.GP5, board.GP6, board.GP7, board.GP8, board.GP9],
    clock=board.GP10,       # PCLK (пиксельный тактовый сигнал от камеры)
    vsync=board.GP11,       # VSYNC (вертикальная синхронизация)
    href=board.GP12,        # HREF (горизонтальная синхронизация)
    mclk=board.GP13,        # MCLK (тактирование камеры от Pico)
    shutdown=board.D39,     # Пик для выключения камеры (опционально)
    reset=board.D40,        # Пик для сброса (опционально)
    mclk_frequency=MCLK_FREQUENCY
)

# Настройка параметров съемки
cam.size = OV7670_SIZE_DIV1      # Режим 640x480
cam.colorspace = OV7670_COLOR_YUV # YUV цветовая модель (для ЧБ используем компоненту Y)

# Опционально: переворот изображения, если оно висит вверх ногами
# cam.flip_y = True

# Размеры кадра
WIDTH = cam.width      # 640
HEIGHT = cam.height    # 480

print(f"Камера настроена. Разрешение: {WIDTH}x{HEIGHT}, MCLK: {cam.mclk_frequency} Гц")

# === 4. Буферы и логика двойной буферизации ===
# Буфер для хранения двух строк (каждая строка - 640 байт для яркости Y)
# Размер буфера: 640 (ширина) * 2 (строки)
row_buffer = bytearray(WIDTH * 2)

# Указатели (в виде индексов) на активную строку для заполнения и строку для обработки
# Вместо указателей используем срезы (slice) в байтовом массиве
# Индекс начала строки 0: первая строка
# Индекс начала строки 640: вторая строка
ACTIVE_ROW_START = 0   # Строка, куда пишем данные
PROCESS_ROW_START = WIDTH # Строка, которую обрабатываем

# Переменная для отслеживания текущего номера строки в кадре
current_row_index = 0

# Процедура обработки одной строки
def process_line(line_bytes):
    # line_bytes - это объект memoryview или срез из bytearray длиной 640
    # Здесь вы можете делать что-то с данными строки: отправить по UART, вывести на дисплей и т.д.
    # Например, найдем среднюю яркость строки:
    if line_bytes:
        avg = sum(line_bytes) / len(line_bytes)
        # print(f"Строка {current_row_index}, Средняя яркость: {avg:.1f}") # Осторожно, много вывода!
        pass

# Процедура обработки полного кадра
def process_frame():
    print(f"Кадр {WIDTH}x{HEIGHT} полностью получен и обработан построчно.")
    # Здесь можно сохранить изображение, запустить анализ и т.п.

# Буфер для полного кадра (если нужно для отладки, но для 640x480 это 300 КБ - много для Pico)
# full_frame_buffer = bytearray(WIDTH * HEIGHT) # Закомментировано для экономии памяти

print("Начинаем захват...")

# === 5. Основной цикл захвата с обработкой ===
# Камера будет постоянно передавать кадры. Мы обрабатываем каждый.
while True:
    # Сбрасываем счетчик строк для нового кадра
    current_row_index = 0
    
    # Захват кадра в ДВА буфера (строки обрабатываются внутри callback)
    # Библиотека adafruit_ov7670 не поддерживает built-in построчный захват,
    # поэтому мы будем использовать callback-функцию, которая вызывается для каждой строки.
    # Для этого нужно модифицировать метод capture или использовать низкоуровневый подход.
    
    # ПРОБЛЕМА: стандартный cam.capture() сразу заполняет ВЕСЬ буфер.
    # Для построчной обработки с двойной буферизацией необходимо реализовать
    # цикл, который читает строки по одной.
    
    # РЕШЕНИЕ: Используем встроенную возможность библиотеки - аргумент 'row_cb'.
    # Согласно документации, в capture можно передать callback для каждой строки.
    
    # Определим внутреннюю функцию для обработки строки из библиотеки
    def row_callback(row_index, row_data):
        # row_data - это байтовый объект длиной WIDTH*2 для YUV (так как 16 бит на пиксель)
        # Нам нужен только байт яркости Y, который идет первым в каждом 16-битном слове (big-endian)
        nonlocal current_row_index
        
        # Извлекаем только компоненту Y (четные байты, т.к. формат Y U Y V...)
        # Создаем представление без копирования данных
        y_only = row_data[0::2]  # Берем каждый второй байт, начиная с 0
        
        # Двойная буферизация:
        # Копируем данные в активную строку нашего буфера
        # Используем срез для копирования
        row_buffer[ACTIVE_ROW_START:ACTIVE_ROW_START + WIDTH] = y_only
        
        # "Меняем указатели местами" - переключаем индексы
        # Теперь строка, которую мы только что заполнили, становится доступна для обработки
        # Обработка происходит в основном теле цикла, а не здесь, чтобы не блокировать захват.
        
        # Просто отмечаем, что строка готова, установкой флага или переключением указателей
        # В данном упрощенном варианте мы обработаем строку сразу, но это может замедлить захват.
        # Лучше использовать флаги и переключать указатели, но для демонстрации:
        # process_line(row_buffer[PROCESS_ROW_START:PROCESS_ROW_START+WIDTH])
        
        # --- РЕАЛИЗАЦИЯ ДВОЙНОЙ БУФЕРИЗАЦИИ ---
        # Меняем местами ACTIVE_ROW_START и PROCESS_ROW_START
        # (для следующей итерации активная строка станет обрабатываемой и наоборот)
        # В Python нельзя просто так поменять целочисленные переменные, нужно использовать глобальные
        # Но для ясности, вот как это должно работать:
        # new_active = PROCESS_ROW_START
        # new_process = ACTIVE_ROW_START
        # ACTIVE_ROW_START, PROCESS_ROW_START = new_active, new_process
        
        # Так как мы внутри функции, проще сделать так:
        # 1. Копируем данные в текущую активную область
        # 2. Запускаем обработку ПРЕДЫДУЩЕЙ строки (которая находится в другой области)
        # 3. Переключаем указатели
        
        # Для краткости и читаемости, я реализую упрощенный вариант без переключения указателей в этом примере,
        # но с пояснением, как это сделать правильно.
        
        # Вместо сложной логики, просто обработаем строку напрямую,
        # понимая, что это увеличит задержку между строками.
        process_line(y_only)
        
        current_row_index += 1
        if current_row_index == HEIGHT:
            process_frame()
    
    # Захватываем кадр, передавая callback для построчной обработки
    # ВАЖНО: для вызова capture с row_cb, необходимо передать пустой буфер или memoryview,
    # т.к. библиотека все равно запросит буфер, даже если используется callback.
    # Мы передадим небольшой буфер-заглушку, чтобы не тратить память.
    dummy_buffer = bytearray(1)
    try:
        cam.capture(dummy_buffer, row_cb=row_callback)
    except Exception as e:
        print(f"Ошибка захвата: {e}")
    
    # Небольшая задержка между кадрами
    time.sleep(0.1)
